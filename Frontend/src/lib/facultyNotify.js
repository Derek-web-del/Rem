export function useFacultyNotify() {
  const { notify } = useNotify()
  return useMemo(
    () => ({
      success: (msg, opts = {}) =>
        notify(msg, { durationMs: FACULTY_TOAST_MS, tone: 'success', ...opts }),
      error: (msg, opts = {}) =>
        notify(msg, { durationMs: FACULTY_TOAST_MS, tone: 'error', ...opts }),
      info: (msg, opts = {}) => notify(msg, { durationMs: FACULTY_TOAST_MS, tone: 'info', ...opts }),
      warning: (msg, opts = {}) =>
        notify(msg, { durationMs: FACULTY_TOAST_MS, tone: 'warning', ...opts }),
    }),
    [notify],
  )
}
